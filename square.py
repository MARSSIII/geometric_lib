def area(a):

    '''
    Принимаем число а (сторона квадрата), возвращает квадрат числа а
    :param a: a - сторона квадрата
    :return: квадрат числа а (площадь квадрата)
    '''

    return a * a


def perimeter(a):

    '''
    Принимает число а, возвращает произведение числа а на 4
    :param a: сторона квадрата
    :return: произведение числа а на 4 (периметр квадрата)
    '''

    return 4 * a
